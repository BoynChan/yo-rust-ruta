pub trait Summary {
    fn summarize(&self) -> String;
}

pub struct NewsArticle {
    pub headline: String,
    pub location: String,
    pub author: String,
    pub content: String,
}

impl Summary for NewsArticle {
    fn summarize(&self) -> String {
        return String::from("");
    }
}

pub struct Tweet {
    pub username: String,
    pub content: String,
    pub reply: bool,
    pub retweet: bool,
}

impl Summary for Tweet {
    fn summarize(&self) -> String {
        return String::from("");
    }
}

pub fn notify(item: &impl Summary) {
    println!("Notify:{}", item.summarize());
}

pub fn notify_trait_bound<T: Summary>(item: &T) {
    println!("Notify:{}", item.summarize());
}

